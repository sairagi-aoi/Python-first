#以下エスケープシーケンス

# 1. 改行（\n）
print("こんにちは\n世界")
print("1行目\n2行目\n3行目")

# 2. タブ文字（\t）
print("名前:\t太郎")
print("年齢:\t25歳")

# 3. クォートの表示
print("彼は「こんにちは」と言いました")
print('彼は\'Hello\'と言いました')
print("彼は\"Hello\"と言いました")

# 4. バックスラッシュの表示
print("ファイルパス: C:\\Users\\Documents\\file.txt")

# 5. 複数のエスケープシーケンスの組み合わせ
print("項目1:\tりんご\n項目2:\tバナナ\n項目3:\tオレンジ")

# 6. 生文字列（raw string）- rプレフィックスを使用
print(r"C:\Users\Documents\file.txt")  # エスケープシーケンスを無効化

# 7. 長い文字列での改行
message = "これは非常に長いメッセージです。\
改行せずに続けて書きたい場合は\
バックスラッシュを行末に使います。"
print(message)

# 8. 三重引用符での複数行文字列
long_text = """これは
複数行にわたる
文字列です。"""
print(long_text)


